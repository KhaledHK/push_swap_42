#include "push_swap.h"

void	sort_small(t_stack **s)
{
	int	max;

	max = find_max(*s);
	if (s[0]-> nb == max)
		ra(s, 1);
	if (s[0]->next->nb == max)
		rra(s, 1);
	if (s[0]-> nb > s[0]-> next -> nb)
		sa(s, 1);
}
